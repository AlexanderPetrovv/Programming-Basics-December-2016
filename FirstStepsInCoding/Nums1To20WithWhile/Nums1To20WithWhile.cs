using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Nums1To20WithWhile
{
    class Nums1To20WithWhile
    {
        static void Main(string[] args)
        {
            int number = 1;
            while (number <= 20)
            {
                Console.WriteLine(number);
                number++;
            }
        }
    }
}